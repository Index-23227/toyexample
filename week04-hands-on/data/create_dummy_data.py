from openpyxl import Workbook


def write_sheet(wb: Workbook, headers: list[str], rows: list[list]) -> None:
    ws = wb.active
    ws.append(headers)
    for row in rows:
        ws.append(row)


def save(filename: str, headers: list[str], rows: list[list]) -> None:
    wb = Workbook()
    write_sheet(wb, headers, rows)
    wb.save(filename)


